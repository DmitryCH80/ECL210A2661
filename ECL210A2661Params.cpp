{
    qint16 val =0;
    *IsOk = false;
    if((command.Params.isEmpty())|
       ((TypeCommand == "READ")&(data.size()!=7))|   // Если ответ на чтение
       ((TypeCommand == "WRITE")&(data.size()!=8)))  // Если ответ на запись
        return val;
    // Считывание значения при команде чтения
    if(TypeCommand == "READ")
    {
        char d = *(data.data() +3);
        char e = *(data.data() +4);
        val ^=d;
        val <<=8;
        val ^=e;
    }
    else // "WRITE"
    {
        char d = *(data.data() +4);
        char e = *(data.data() +5);
        val ^=d;
        val <<=8;
        val ^=e;
    }
    // Проверка в диапазоне ли параметр
    //if(TestValRange(index,val))
        *IsOk = true;
    return val;
}
