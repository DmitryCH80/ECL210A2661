// Методы класса CParam
CParam::CParam(ECL210A2661Config *pconfig, quint32 pIndex, quint32 pTPointIndex)
{
    pConfig = pconfig;
    // Индекс вне пределов параметров
    if((pIndex <0)|(pIndex >= ParamRegisters.size()))
    {
        AddrH       =0;
        AddrL       =0;
        IsInit      =false;
        Index       =0;
        TPointIndex =0;
        return;
    }
    // Индекс 19 - температурный график
    else if((pIndex ==19)&((pTPointIndex <0)|(pTPointIndex >= ParamTPointRegisters.size())))
    {
        AddrH       =0;
        AddrL       =0;
        IsInit      =false;
        Index       =0;
        TPointIndex =0;
        return;
    }
    // Индекс корректен - заполнение данными
    Index       = pIndex;
    TPointIndex = pTPointIndex;
    // Адрес регистра
    if(pIndex!=19)
    {
        AddrH = ParamRegisters.at(Index).R8H;
        AddrL = ParamRegisters.at(Index).R8L;
        // Значение по умолчанию - заводское
        QString txtParam = FactoryParams.at(Index);
        IsInit = true;
    }
    else
    {
        AddrH = ParamTPointRegisters.at(TPointIndex).R8H;
        AddrL = ParamTPointRegisters.at(TPointIndex).R8L;
        // Значение по умолчанию - заводское
        QString txtParam = FactoryTPointsHeat.at(TPointIndex);
        IsInit = true;
    }
}
// Получение параметра из текстовой строки и преобразование его к формату для передачи регулятору
qint16 CParam::GetValFromText(QString text, quint32 index, bool *IsOk)
{
    *IsOk = false;
    qint16 val;
    float fval;
    // Индексы параметров значение которых может быть "Выкл"
    if((index ==2)|(index ==17)|(index ==26))
    {
        // Если выкл - то значение 0
        if(text.contains("ВЫКЛ", Qt::CaseInsensitive))
        {
            *IsOk = true;
            return 0;
        }
        // Иначе от 0 до 50
        val = text.toInt(IsOk);
        if((val <0)|(val >50)|((*IsOk)!=true))
            return FactoryParamsInt.at(index);
        *IsOk = true;
        return val;
    }
    // Параметры значения которых от 10 до 150
    if((index ==0)|(index ==1)|(index ==12)|(index ==14)|(index ==21)|(index ==22)|(index ==23)|
       (index ==27)|(index ==28))
    {
        val = text.toInt(IsOk);
        if((val <10)|(val >150)|(!IsOk))
        {
            return FactoryParamsInt.at(index);
        }
        *IsOk = true;
        if((index!=21)&(index!=22))
            return val;
        return (val*10);
    }
    // Параметры значения которых от 5 до 250
    if((index ==7)|(index ==9)|(index ==29)|(index ==31))
    {
        val = text.toInt(IsOk);
        if((val <5)|(val >250)|((*IsOk)!=true))
            return FactoryParamsInt.at(index);
        *IsOk = true;
        return val;
    }
    // Параметры от 5.0 до 40.0
    if((index ==3)|(index ==4))
    {
        fval = text.toFloat(IsOk);
        fval *=10;
        val = 0 + fval;
        if((val < 50)|(val >400)|((*IsOk)!=true))
            return FactoryParamsInt.at(index);
        *IsOk = true;
        return val;
    }
    // Параметр от -9.9 до 0.0
    if(index ==5)
    {
        fval = text.toFloat(IsOk);
        fval *=10;
        val = 0 + fval;
        if((val < (-99))|(val >0)|((*IsOk)!=true))
            return FactoryParamsInt.at(index);
        *IsOk = true;
        return val;
    }
    // Параметр от 0.0 до 9.9
    if(index ==6)
    {
        fval = text.toFloat(IsOk);
        fval *=10;
        val = 0 + fval;
        if((val < 0)|(val >99)|((*IsOk)!=true))
            return FactoryParamsInt.at(index);
        *IsOk = true;
        return val;
    }
    // Параметры значения которых от 1 до 999
    if((index ==8)|(index ==30))
    {
        val = text.toInt(IsOk);
        if((val <1)|(val >999)|((*IsOk)!=true))
            return FactoryParamsInt.at(index);
        *IsOk = true;
        return val;
    }
    // Параметры значения которых от 1 до 9
    if((index ==10)|(index ==32))
    {
        val = text.toInt(IsOk);
        if((val <1)|(val >9)|((*IsOk)!=true))
            return FactoryParamsInt.at(index);
        *IsOk = true;
        return val;
    }
    // Параметры значения которых от -60 до 20
    if((index ==11)|(index ==13))
    {
        val = text.toInt(IsOk);
        if((val <(-60))|(val >20)|((*IsOk)!=true))
            return FactoryParamsInt.at(index);
        *IsOk = true;
        return val;
    }
    // Параметры от -9.9 до 9.9
    if((index ==15)|(index ==16)|(index ==24)|(index ==25))
    {
        fval = text.toFloat(IsOk);
        fval *=10;
        val = 0 + fval;
        if((val < (-99))|(val >99)|((*IsOk)!=true))
            return FactoryParamsInt.at(index);
        *IsOk = true;
        return val;
    }
    // Параметры значения которых от 2 до 50
    if((index ==18)|(index ==33))
    {
        val = text.toInt(IsOk);
        if((val <2)|(val >50)|((*IsOk)!=true))
            return FactoryParamsInt.at(index);
        *IsOk = true;
        return val;
    }
    // Параметры значения которых от 1 до 5
    if((index ==20)|(index ==34))
    {
        val = text.toInt(IsOk);
        if((val <1)|(val >5)|((*IsOk)!=true))
            return FactoryParamsInt.at(index);
        *IsOk = true;
        return val;
    }
    // Точки температурного графика (должны быть в диапазоне Tпод_мин, Тпод_макс
    // Параметры значения которых от 10 до 150
    if(index ==19)
    {
        val = text.toInt(IsOk);
        if((val <10)|(val >150)|((*IsOk)!=true))
            return FactoryTPointsHeatInt.at(0);
        *IsOk = true;
        return val;
    }
    return 0;
}
