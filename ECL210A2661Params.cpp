// Получение команды
CCommandDB CParam::GetCommand(QString cType, quint32 index, quint32 gindex, qint16 value)
{
    CCommandDB Command;
    // Заполнение данных нулевой команды
    InitNullCommand(Command);
    QByteArray data;
    data.clear();
    // Если ошибка в передаваемых аргументах или параметр не инициализирован, то возврат нулевой команды
    if(((cType !="READ")&(cType !="WRITE"))|(IsInit == false))
        return Command;
    if(index!=19)
    {
        AddrH = ParamRegisters.at(index).R8H;
        AddrL = ParamRegisters.at(index).R8L;
    }
    else
    {
        AddrH = ParamTPointRegisters.at(gindex).R8H;
        AddrL = ParamTPointRegisters.at(gindex).R8L;
    }
    char c[8] ="";           // Команды имеют одинаковую длину
    quint8 d = pConfig->ModBusAddr.toUInt();
    c[0] = 0x00;
    c[0] ^=d;                 // ModBus-адрес регулятора
    if(cType == "READ")
    {
        // Подготовка команды
        c[1] =0x04;           // Команда чтения
        c[2] =0x00;           // Старшая часть регистра
        c[2] ^=AddrH;
        c[3] =0x00;           // Младшая часть регистра
        c[3] ^=AddrL;
        c[4] =0x00;           // Старшая часть числа читаемых регистров
        c[5] =0x01;           // Младшая часть числа читаемых регистров
        c[6] = c[7] = 0x00;
        GetCRC16((unsigned char*)c, (unsigned char*)(c+7),(unsigned char*)(c+6), sizeof(c));
        data.append(c,sizeof(c));
        Command.Command = data;
        Command.CID = GetCID(index, gindex);
        Command.Params.append(GetCVarDBRead());
        return Command;
    }
    // Подготовка команды записи
    c[1] =0x06;           // Команда записи
    c[2] =0x00;           // Старшая часть регистра
    c[2] ^=AddrH;
    c[3] =0x00;           // Младшая часть регистра
    c[3] ^=AddrL;
    c[4] =0x00;           // Старшая часть устанавливаемого параметра
    c[4] ^=*(((quint8*)&value)+1);
    c[5] =0x00;           // Младшая часть устанавливаемого параметра
    c[5] ^=*((quint8*)&value);
    c[6] = c[7] = 0x00;
    GetCRC16((unsigned char*)c, (unsigned char*)(c+7),(unsigned char*)(c+6), sizeof(c));
    data.append(c,sizeof(c));
    Command.Command = data;
    Command.CID = GetCID(index, gindex);
    Command.Params.append(GetCVarDBWrite());
    return Command;
}
